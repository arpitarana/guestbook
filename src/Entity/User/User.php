    {
        return $this->guestDetails;
    }

    /**
     * Add guestDetail
     *
     * @param \App\Entity\Guest\GuestDetail $guestDetail
     *
     * @return User
     */
    public function addGuestDetail(\App\Entity\Guest\GuestDetail $guestDetail)
    {
        $this->guestDetails[] = $guestDetail;

        return $this;
    }
