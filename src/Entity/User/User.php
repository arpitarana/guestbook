    /**
     * Remove guestDetail
     *
     * @param \App\Entity\Guest\GuestDetail $guestDetail
     */
    public function removeGuestDetail(\App\Entity\Guest\GuestDetail $guestDetail)
    {
        $this->guestDetails->removeElement($guestDetail);
